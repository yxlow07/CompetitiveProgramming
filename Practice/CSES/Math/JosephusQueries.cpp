int calculateChildrenLeft(int n, int k) {
    if (n == 1) return 1;

    if (k <= (n+1)/2) {
        if (2 * k > n) return (2 * k) % n;
        else return 2 * k;
    }

    int childrenLeft = calculateChildrenLeft(n/2, k - (n + 1) / 2 );
    if (n%2==1) return 2 * childrenLeft + 1;
    return 2 * childrenLeft - 1;
}

void solve() {
    int n, k; cin>>n>>k;
    cout<<calculateChildrenLeft(n, k)<<nl;
}

